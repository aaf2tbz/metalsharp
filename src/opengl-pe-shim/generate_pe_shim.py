#!/usr/bin/env python3
"""Regenerate FORWARD_GL_VOID stubs for the opengl32 PE shim.

Reads src/opengl/EntryPoint.cpp, extracts every gl[A-Z][A-Za-z0-9]+ symbol
that looks like an exported GL entry point, and prints FORWARD_GL_VOID
stubs to stdout (one per line, sorted, deduplicated). This is a helper
script: the checked-in opengl32_pe_shim.c is the source of truth; this
script exists so future GL export changes can be replayed without
hand-editing the .c file.

Usage:
    python3 src/opengl-pe-shim/generate_pe_shim.py \\
        --entrypoint src/opengl/EntryPoint.cpp \\
        > /tmp/opengl_stubs.txt
"""

from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path

GL_NAME_RE = re.compile(r"\bgl[A-Z][A-Za-z0-9_]*\b")
HEADER = (
    "/* Generated by generate_pe_shim.py - do not hand-edit. */\n"
    "#include \"opengl32_pe_shim.h\"\n\n"
)


def extract_names(source: str) -> list[str]:
    seen: set[str] = set()
    for match in GL_NAME_RE.finditer(source):
        seen.add(match.group(0))
    return sorted(seen)


def render_stubs(names: list[str]) -> str:
    lines = [HEADER]
    for name in names:
        lines.append(f"FORWARD_GL_VOID({name})")
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--entrypoint",
        type=Path,
        default=Path("src/opengl/EntryPoint.cpp"),
        help="Path to the macOS opengl EntryPoint.cpp source.",
    )
    args = parser.parse_args()
    if not args.entrypoint.is_file():
        print(f"error: entrypoint source not found: {args.entrypoint}", file=sys.stderr)
        return 1
    source = args.entrypoint.read_text(encoding="utf-8", errors="replace")
    names = extract_names(source)
    sys.stdout.write(render_stubs(names))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
